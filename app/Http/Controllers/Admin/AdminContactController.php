    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        // validate form
        $request->validate([
            'name' => 'required',
            'email' => 'required',
            'message' => 'required',
        ]);

        Contact::create([
            'name' => $request->name,
            'email' => $request->email,
            'message' => $request->message,
        ]);

        // toastr notification
        $notification = array (
            'message' => 'Send Message Successfully!',
            'alert-type' => 'success'
        );

        // redirect back
        return back()->with($notification);
    }

